public class Vector2D
{
    public double x;
    public double y;
    
    public Vector2D(double x1, double y1)
    {
        x = x1;
        y = y1;
    }
    
    public Vector2D()
    {
    }
    
    public void setX(double x1)
    {
        x = x1;
    }
    
    public void setY(double y1)
    {
        y = y1;
    }
}
